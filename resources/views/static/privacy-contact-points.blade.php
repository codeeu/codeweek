            <h4 id="#header-1"><strong>1. Introduction</strong></h4>
            <p>The Code4Europe is committed to protecting your personal data and to respecting your privacy. The Code4Europe
                collects and further processes personal data pursuant to <a
                    href="https://eur-lex.europa.eu/legal-content/EN/TXT/?uri=CELEX%3A32016R0679&amp;qid=1716801254470">Regulation
                    (EU) 2016/679</a> of the European Parliament and of the Council, of 27 April 2016, on the protection of
                natural persons with regard to the processing of personal data and on the free movement of such data, and
                repealing Directive 95/46/EC (hereafter, Regulation (EU) 2016/679).</p>
            <p>This privacy statement explains the reason for the processing of your personal data, the way we collect,
                handle and ensure protection of all personal data provided, how that information is used and what rights you
                have in relation to your personal data. It also specifies the contact details of the responsible joint
                controllers with whom you may exercise your rights, the Data Protection Officers and the relevant Data
                Protection Authorities.</p>
            <h4 id="#header-2"><strong>2. Why and how do we process your personal data?</strong></h4>The controller collects the personal
                data of activity organisers in order to be able to issue certificates, to publish the registered activities
                on the EU Code Week map (<a href="https://codeweek.eu/events">https://codeweek.eu/events</a>) and to conduct
                statistical analyses. </p>
            <p>The controller publishes the personal data of EU Code Week ambassadors and leading teachers in the community
                section of the EU Code Week website (<a
                    href="https://codeweek.eu/community">https://codeweek.eu/community</a>) to facilitate the identification
                of contact points by citizens, stakeholders as well as the joint controllers. Offering contacts with
                individuals rather than functional mailboxes is often the best and most efficient way to ensure that
                interested people can get in contact with the relevant EU Code Week contact points at national level. </p>
            <p>The controller processes the personal data of edu coordinators in order to get in touch with those contact
                points. </p>
            <p>Personal data will not be used for an automated decision-making, including profiling.</p>
            <h4 id="#header-3"><strong>3. On what legal ground(s) do we process your personal data?</strong></h4>The controller collects,
                stores and publishes your personal data, because you gave your explicit consent, for example by ticking a
                box when you signed up to become an EU Code Week Ambassador, Leading Teacher or when you applied to register
                an event on the <a href="https://codeweek.eu/register">EU Code Week</a> map. Edu coordinators provided their
                consent via email or filling in an EU Survey.</p>
            <h4 id="#header-4"><strong>4. Which personal data do we collect and further process?</strong></h4>
            <p>The <strong>mandatory</strong> personal data collected for <strong>Ambassadors and Leading Teachers</strong>
                are:</p>
            <ul>
                <li>Name </li>
                <li>Email address </li>
                <li>Picture </li>
                <li>Biography </li>
                <li>Title </li>
                <li>Field of expertise for Leading Teachers</li>
            </ul>
            <p>The <strong>mandatory</strong> personal data collected for <strong>Edu coordinators</strong> are:</p>
            <ul>
                <li>Name </li>
                <li>Email address </li>
                <li>Affiliation</li>
            </ul>
            <p>When you <strong>register an activity, the mandatory</strong> personal data collected are:</p>
            <ul>
                <li>Name of the organisation </li>
                <li>Type of organisation </li>
                <li>Contact e-mail address</li>
            </ul>
            <p>The provision of this data is mandatory to be an EU Code Week Ambassador, an Edu Coordinator, or Leading
                Teacher; or to register an activity on the Code Week website.</p>
            <p>In addition, <strong>Ambassadors and Leading Teachers</strong> can <strong>voluntarily</strong> provide the
                following personal data:</p>
            <ul>
                <li>Social media accounts</li>
            </ul>
            <p><strong>Activity organisers</strong> can <strong>voluntarily</strong> provide the following data:</p>
            <ul>
                <li>Public contact e-mail address </li>
                <li>Organiser’s website </li>
                <li>Code Week for All Code</li>
            </ul>
            <p>For activity organisers, please note that relevant Ambassador for your country, use the personal data you
                provided to approve an activity and may contact you with regard to that activity.</p>
            <p>You provided your personal data on the <a href="https://codeweek.eu/login">Code Week Website login page</a>,
                on the <a href="https://codeweek.eu/add">activity registration page or by filling in an EU survey or
                    replying to an email.</a></p>
            <h4 id="#header-5"><strong>5. How long do we keep your personal data?</strong></h4>
            <p>The controller only keeps your personal data for the time necessary to fulfil the purpose of collection or
                further processing. All personal data will be deleted from database in December 2029 or until you withdraw
                from the EU Code Week initiative.</p>
            <h4 id="#header-6"><strong>6. How do we protect and safeguard your personal data?</strong></h4>All personal data in electronic
                format (e-mails, documents, databases, uploaded batches of data, etc.) are stored on the servers
                administered by the Commission. All processing operations are carried out pursuant to the <a
                    href="https://eur-lex.europa.eu/legal-content/EN/TXT/?qid=1548093747090&amp;uri=CELEX:32017D0046">Commission
                    Decision (EU, Euratom) 2017/46</a>, of 10 January 2017, on the security of communication and information
                systems in the European Commission.</p>
            <p>In order to protect your personal data, the controller have put in place a number of technical and
                organisational measures. Technical measures include appropriate actions to address online security, risk of
                data loss, alteration of data or unauthorised access, taking into consideration the risk presented by the
                processing and the nature of the personal data being processed. Organisational measures include restricting
                access to the personal data solely to authorised persons with a legitimate need to know for the purposes of
                this processing operation.</p>
             <h4 id="#header-7"><strong>7. Who has access to your personal data and to whom is it disclosed?</strong></h4>
            <p>Access to your personal data for the purposes of managing EU Code Week Ambassadors, Leading Teachers, Edu
                Coordinators and handling of registered activities on the website is only provided to the staff of the
                Code4Europe consortium responsible for carrying out this processing operation, based on the “need to know”
                principle.<br>Code4Europe will contact those Ambassadors, Leading Teachers and Edu coordinators who have
                explicitly provided their consent to transfer their contact details to Code4Europe (e.g. for inviting them
                to online and physical community meetings, etc.). All EU Code Ambassadors and Leading Teachers that have not
                provided their consent to transfer their contact details to Code4Europe will be deleted from the community
                page of the website.</p>
            <p>For the purpose of increasing the Code Week Ambassador and Leading Teacher’s or the registered activities’
                visibility and facilitating the identification of those contact points by citizens and stakeholders (e.g. to
                find out more about the initiative at local level, get help for the organisation of activity, etc.),
                personal data including the name, the email address, a picture, biography, the title and the field of
                expertise for Leading Teachers are published on the following <a href="https://codeweek.eu/">website</a>.
            </p>
            <p>In addition, the controller might share your contact details with any member of the public or stakeholder
                that would need to contact you with regard to the EU Code Week. For activity organisers, please note that
                the relevant EU Code Week Ambassador for your country, may use the personal data you provided to register an
                activity to contact you with regard to said activity.</p>
             <h4 id="#header-8"><strong>8. What are your rights and how can you exercise them?</strong></h4>
            <p>You have specific rights as a ‘data subject’ under Chapter III Regulation (EU) 2016/679, in particular the
                right to access, rectify or erase your personal data and the right to restrict the processing of your
                personal data. Where applicable, you also have the right to object to the processing or the right to data
                portability.</p>
            <p>You have the <strong>right to object to the processing of your personal data</strong>, which is lawfully
                carried out pursuant to Article 5(1)(a) of Regulation (EU) 2016/679.</p>
            <p>For those processing activities which require your consent, you have the <strong>right to withdraw your
                    consent at any time</strong> by notifying the joint controllers The withdrawal will not affect the
                lawfulness of the processing carried out before you have withdrawn the consent.</p>
            <p>You can exercise your rights by contacting the controller, or in case of conflict the Data Protection
                Officer. If necessary, you can also address the Data Protection Authority of Belgium. The contact
                information is given under section 9 below.</p>
            <p>Where you wish to exercise your rights in the context of one or several specific processing operations,
                please provide their description (i.e. their Record reference(s) as specified under section 10 below) in
                your request.</p>

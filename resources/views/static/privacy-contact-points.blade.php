            <h3 id="header-2"><strong>2. Why and how do we process your personal data?</strong></h3>
            <p>
                The joint controller collect the personal data of activity organisers in order to be able to issue
                certificates, to publish the registered activities on the <a href="https://codeweek.eu/events">EU Code
                    Week map</a> and to conduct statistical analyses.
            </p>
            <p> The joint controllers publish the personal data of EU Code Week Ambassadors and Leading Teachers in the
                <a href="https://codeweek.eu/community?country_iso=BE">community section</a> of the EU Code Week website
                to facilitate the identification of contact points by citizens, stakeholders as well as the joint
                controllers. Offering contacts with individuals rather than functional mailboxes is often the best and
                most efficient way to ensure that interested people can get in contact with the relevant EU Code Week
                contact points at national level.
            </p>
            <p>The joint controllers process the personal data of Edu Coordinators in order to get in touch with those
                contact points.
            </p>
            <p>Personal data will not be used for an automated decision-making, including profiling.
            </p>

            <h3 id="header-3"><strong>3. On what legal ground(s) do we process your personal data?</strong></h3>
            <p>The joint controllers collect, store and publish your personal data, because you gave your explicit
                consent, for example by ticking a box when you signed up to become an EU Code Week Ambassador, Leading
                Teacher or when you applied to register an event on the <a href="https://codeweek.eu/events">EU Code
                    Week map</a>. Edu coordinators provided their consent via email or filling in an EU Survey.</p>



            <h3 id="header-6"><strong>6. How do we protect and safeguard your personal data?</strong></h3>
            <p>All personal data in electronic format (e-mails, documents, databases, uploaded batches of data, etc.)
                are stored on servers administered by the Commission. All processing operations are carried out pursuant
                to the <a href="https://eur-lex.europa.eu/legal-content/EN/TXT/?qid=1548093747090&uri=CELEX:32017D0046">Commission
                    Decision (EU, Euratom) 2017/46</a>, of 10 January 2017, on the security of communication and
                information systems in the European Commission.
            </p>
            <p>In order to protect your personal data, the joint controllers have put in place a number of technical and
                organisational measures. Technical measures include appropriate actions to address online security, risk
                of data loss, alteration of data or unauthorised access, taking into consideration the risk presented by
                the processing and the nature of the personal data being processed. Organisational measures include
                restricting access to the personal data solely to authorised persons with a legitimate need to know for
                the purposes of this processing operation.</p>


            <h3 id="header-7"><strong>7. Who has access to your personal data and to whom is it disclosed?</strong></h3>
            <p>Access to your personal data for the purposes of managing EU Code Week Ambassadors, Leading Teachers, Edu Coordinators and handling of registered activities on the website is only provided to the Commission staff and staff of the Code4Europe consortium responsible for carrying out this processing operation, based on the “need to know” principle.
                Code4Europe will contact those Ambassadors, Leading Teachers and Edu coordinators who have explicitly provided their consent to transfer their contact details to Code4Europe (e.g. for inviting them to online and physical community meetings, etc.). All EU Code Ambassadors and Leading Teachers that have not provided their consent to transfer their contact details to Code4Europe will be deleted from the community page of the website.</p>
            <p>For the purpose of increasing the Code Week Ambassador and Leading Teacher’s or the registered activities’ visibility and facilitating the identification of those contact points by citizens and stakeholders (e.g. to find out more about the initiative at local level, get help for the organisation of activity, etc.), personal data including the name, the email address, a picture, biography, the title and the field of expertise for Leading Teachers are published on the following <a href="https://codeweek.eu">website</a>. </p>
            <p>In addition, the joint controllers might share your contact details with any member of the public or stakeholder that would need to contact you with regard to the EU Code Week. For activity organisers, please note that the relevant EU Code Week Ambassador for your country, may use the personal data you provided to register an activity to contact you with regard to said activity.</p>



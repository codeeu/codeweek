                    <p>Em 2018, a Semana Europeia da Programa&ccedil;&atilde;o ir&aacute; decorrer entre <strong>6 e 21 de outubro</strong>.</p>

                    <p>A Semana Europeia da Programa&ccedil;&atilde;o &eacute; uma iniciativa de base que celebra a criatividade, a resolu&ccedil;&atilde;o de problemas e a colabora&ccedil;&atilde;o atrav&eacute;s da programa&ccedil;&atilde;o e de outras atividades tecnol&oacute;gicas. A ideia &eacute; tornar a programa&ccedil;&atilde;o mais vis&iacute;vel, mostrar aos jovens, adultos e idosos a forma de dar vida &agrave;s ideias atrav&eacute;s da programa&ccedil;&atilde;o, desmistificar estas compet&ecirc;ncias e reunir pessoas motivadas para aprender.</p>

                    <h2>Executada por volunt&aacute;rios</h2>

                    <p>A Semana Europeia da Programa&ccedil;&atilde;o &eacute; levada a cabo por volunt&aacute;rios. Um ou mais <a href="/ambassadors">embaixadores da Semana da Programa&ccedil;&atilde;o</a> coordenam a iniciativa nos seus pa&iacute;ses, mas qualquer pessoa pode organizar a sua pr&oacute;pria atividade e adicion&aacute;-la ao mapa da <a href="/">codeweek.eu</a>.</p>

                    <h2>Apoiada pela Comiss&atilde;o Europeia</h2>

                    <p>A Semana Europeia da Programa&ccedil;&atilde;o foi lan&ccedil;ada em 2013 pelo grupo Young Advisors for the Digital Agenda Europe. A Comiss&atilde;o Europeia apoia a Semana Europeia da Programa&ccedil;&atilde;o no &acirc;mbito da sua estrat&eacute;gia para o <a href="http://ec.europa.eu/priorities/digital-single-market/">Mercado &Uacute;nico Digital</a>. No <a href="https://ec.europa.eu/education/initiatives/european-education-area/digital-education-action-plan_en">Plano de A&ccedil;&atilde;o para a Educa&ccedil;&atilde;o Digital</a>, a Comiss&atilde;o incentiva especialmente as escolas a aderirem &agrave; iniciativa. O objetivo &eacute; alcan&ccedil;ar 50&nbsp;% do conjunto das escolas da Europa at&eacute; 2020.</p>

                    <h2>Escolas</h2>

                    <p>As escolas de todos os n&iacute;veis de ensino e os professores de todas as disciplinas s&atilde;o particularmente convidados a participarem na Semana Europeia da Programa&ccedil;&atilde;o, para proporcionar a oportunidade aos seus alunos de explorarem a criatividade digital e a programa&ccedil;&atilde;o. Saiba mais sobre a iniciativa e sobre como organizar a sua atividade atrav&eacute;s p&aacute;gina Web dedicada aos professores: <a href="/schools">CodeWeek.eu/Schools</a></p>


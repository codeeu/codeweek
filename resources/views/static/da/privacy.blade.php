            <h1 style="margin-bottom:10px;">BESKYTTELSE AF DINE PERSONOPLYSNINGER</h1>

            <h3><strong>1. Indledning</strong></h3>

            <p>Det er vigtigt for Europa-Kommissionen (herefter "Kommissionen") at beskytte dine personoplysninger og respektere din ret til privatliv. Kommissionen indsamler og viderebehandler personoplysninger i henhold til Europa-Parlamentets og Rådets forordning (EU) 2018/1725 af 23. oktober 2018 om beskyttelse af fysiske personer i forbindelse med behandling af personoplysninger i Unionens institutioner, organer, kontorer og agenturer og om fri udveksling af sådanne oplysninger (som ophæver forordning (EF) nr. 45/2001).</p>

            <p>Denne erklæring om beskyttelse af personoplysninger forklarer årsagerne til indsamlingen og behandlingen, den måde hvorpå vi indsamler, håndterer og sikrer beskyttelse af alle afgivne personoplysninger, hvordan disse oplysninger bruges samt de rettigheder, du kan udøve i forbindelse med dine personoplysninger (ret til indsigt, berigtigelse, blokering osv.). Den indeholder desuden kontaktoplysninger til den dataansvarlige, som du kan henvende dig til for at udøve dine rettigheder, databeskyttelsesrådgiveren og Den Europæiske Tilsynsførende for Databeskyttelse.</p>

            <p>Denne erklæring om beskyttelse af personoplysninger vedrører indsamling og offentliggørelse af personoplysninger på det offentligt tilgængelige websted Codeweek.eu, for personer, der fungerer som kontaktpunkter for EU's kodeugeaktiviteter (ambassadører for kodeugen, undervisningsministeriekoordinatorer, undervisningsvejledere samt personer, der afholder aktiviteter og arrangementer).</p>



            <h3><strong>2. Hvorfor behandler vi dine data?</strong></h3>

            <p>Europa-Kommissionen indsamler og offentliggør dine personoplysninger for at gøre det lettere for interessenter eller interesserede borgere at identificere kontaktpunkter. At tilbyde rigtige personer som kontaktpunkter er den bedste og mest effektive måde at sikre, at interesserede kan komme i kontakt med Kommissionens tjenester.</p>

            <p>Dine personoplysninger vil ikke blive anvendt til automatiske afgørelser, herunder profilering.</p>




            <h3><strong>3. Retsgrundlag for at behandle dine personoplysninger</strong></h3>

            <p>Vores behandling af personoplysninger med henblik på offentliggørelse af kontaktpunkter er lovlig i henhold til artikel 5, stk. 1, litra d), i forordning (EU) 2018/1725, fordi du har givet samtykke til behandling af dine personoplysninger via en webformular, eller da du accepterede, at vi bruger din e-mailadresse og dit brugernavn, hvis du tilmeldte dig via et socialt netværk.</p>



            <h3><strong>4. Hvilke personoplysninger indsamler og viderebehandler vi?</strong></h3>

            <p>De indsamlede personoplysninger er oplysninger, som gør det lettere at identificere dig som kontaktpunkt, og som øger din synlighed for offentligheden. Det drejer sig om: titel, fornavn, efternavn, stilling, arbejdsadresse (inklusive postnummer), telefonnummer, billede, konto på sociale medier og biografi.</p>

            <p>Du afgav disse oplysninger frivilligt, da du udfyldte ansøgningsformularen.</p>

            <p>Det er obligatorisk at afgive visse personoplysninger med henblik på at gøre det muligt at offentliggøre aktiviteter og/eller kontaktoplysninger på webstedet codeweek.eu. Hvis du ikke afgiver dine personoplysninger, kan din aktivitet ikke offentliggøres, og/eller du vil ikke kunne deltage i ovennævnte netværk.</p>

            <p>Du afgiver alle andre personoplysninger frivilligt.</p>



            <h3><strong>5. Hvor længe beholder vi dine personoplysninger?</strong></h3>

            <p>Kommissionen beholder kun dine personoplysninger så lang tid, det er nødvendigt for at opfylde formålet med indsamlingen eller viderebehandlingen som beskrevet i punkt 2, dvs. så længe du fungerer som kontaktpunkt.</p>

            <p>Dine personoplysninger fjernes fra det offentligt tilgængelige websted, så snart du ikke længere fungerer som kontaktpunkt, medmindre du har givet tilladelse til, at vi beholder dine oplysninger i databasen med henblik på fremtidige aktiviteter.</p>



            <h3><strong>6. Hvordan beskytter og sikrer vi dine personoplysninger?</strong></h3>

            <p>Alle personoplysninger i elektronisk format (e-mails, dokumenter, databaser, uploadede batcher af data osv.) gemmes enten på Europa-Kommissionens servere eller på en af dens underleverandørers servere. Al behandling udføres i henhold til Kommissionens afgørelse (EU, Euratom) 2017/46 af 10. januar 2017 om kommunikations- og informationssystemernes sikkerhed i Europa-Kommissionen.</p>

            <p>Kommissionens underleverandører er bundet af en specifik kontraktbestemmelse vedrørende al behandling af dine oplysninger på vegne af Kommissionen og af fortrolighedsforpligtelserne, der følger af gennemførelsen af den generelle forordning om databeskyttelse i EU's medlemsstater ("GDPR-forordningen", forordning (EU) 2016/679).</p>

            <p>For at beskytte dine personoplysninger har Kommissionen indført en række tekniske og organisatoriske foranstaltninger. De tekniske foranstaltninger er blandt andet passende handlinger vedrørende internetsikkerhed, risiko for datatab, ændring af data eller uautoriseret adgang, under hensyntagen til den risiko, som behandlingen indebærer, og arten af de behandlede personoplysninger. Organisatoriske foranstaltninger er blandt andet begrænsning af adgangen til personoplysningerne alene til personer, som har et legitimt behov for at kende dem i forbindelse med formålet med behandlingen.</p>




            <h3><strong>7. Hvem har adgang til dine oplysninger, og hvem videregives de til?</strong></h3>

            <p>Adgang til dine personoplysninger gives til det personale hos Kommissionen, der har ansvar for at udføre behandlingen, og til autoriseret personale ud fra princippet om "behov for at vide". Dette personale er bundet af lovpligtige, og hvor relevant andre, fortrolighedsaftaler.</p>

            <p>Nærmere bestemt kan alle dine afgivne personoplysninger tilgås af webstedernes administratorer (Kommissionens personale) samt andet personale hos Kommissionen, hvis disse har "behov for at vide". . Endvidere deles dine personoplysninger og oplysninger om arrangementer med medlemmer af netværkene af ambassadører for EU’s kodeuge og undervisningsministeriekoordinatorer med henblik på afholdelse af lokale aktiviteter eller opfølgning.</p>

            <p>For at gøre kontaktpunkterne mere synlige offentliggøres dine personoplysninger uden adgangsbegrænsning på det offentlige websted: https://codeweek.eu.</p>

            <p><i>Vedrørende overførsel af oplysninger til tredjeparter</i></p>

            <p>De oplysninger, vi indsamler, vil ikke blive videregivet til tredjepart, undtagen i det omfang det er lovpligtigt for os at gøre det.</p>




            <h3><strong>8. Hvad er dine rettigheder, og hvordan kan du udøve dem?</strong></h3>

            <p>Du har særlige rettigheder som "registreret" i henhold til kapitel III (artikel 14-25) i forordning (EU) 2018/1725, især hvad angår ret til indsigt, berigtigelse eller sletning af dine personoplysninger samt ret til at begrænse behandling af dine personoplysninger. Hvor det måtte være relevant, har du også ret til at gøre indsigelse mod behandling eller ret til dataportabilitet.</p>
            <p>Du har givet samtykke til, at vi må behandle dine personoplysninger, og du kan når som helst trække dit samtykke tilbage ved at give den dataansvarlige besked. Tilbagetrækningen berører ikke lovligheden af den behandling, der er udført, inden du trak dit samtykke tilbage.</p>
            <p>Du kan udøve dine rettigheder ved at kontakte den dataansvarlige. I tilfælde af en konflikt skal du kontakte databeskyttelsesrådgiveren. Om nødvendigt kan du også henvende dig til Den Europæiske Tilsynsførende for Databeskyttelse. Deres kontaktoplysninger findes i punkt 9 nedenfor.</p>




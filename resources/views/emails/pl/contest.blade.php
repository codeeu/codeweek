Szanowni Państwo!

Dziękujemy za udział w CodeWeek 2022! Dzięki Wam Polska zajmuje obecnie 3. miejsce na podium i ciągle mamy szanse na wyższe miejsce. Inicjatywy można zgłaszać jeszcze do 31 grudnia 2022. Jeśli więc ktoś z Was planuje nadal uczyć programowania lub uczyć się programować ciągle ma szansę dołączyć do CodeWeek2022 i <strong>wziąć udział w konkursie</strong> „1000 pierwszych organizatorów”. 😊

<h1>O konkursie</h1>

O co chodzi? 1000 pierwszych osób, które zgłoszą wydarzenie rejestrując je na mapę CodeWeek, a następnie przekażą link do swojego wydarzenia za pośrednictwem poniższego formularza konkursowego otrzyma nagrodę - <strong>pakiet 10 gier Cody Roby</strong>, które mamy nadzieję będą Państwu pomocą w dalszych działaniach związanych z promocją programowania.

Konkurs trwa do czasu wyłonienia 1000 pierwszych autorów inicjatyw, jednak nie <strong>później niż do 23 listopada, do godz. 23:59</strong>. Wyniki opublikowane zostaną na stronie koduj.gov.pl do dnia 10 grudnia.

<h1>Zadanie konkursowe</h1>

Jeśli jeszcze nie wzięliście udziału w konkursie – warto to nadrobić:
Zgłoście opublikowane wydarzenie za pośrednictwem <a href="https://codeweek2022-konkurs.webankieta.pl/">formularza konkursowego</a> (przesyłając link), podając dane do wysyłki nagrody. Jeśli znajdziecie się w pierwszym tysiącu organizatorów, po rozstrzygnięciu konkursu (które nastąpi najpóźniej do 10 grudnia 2022 r.) wyślemy Wam nagrodę i poinformujemy o tym e-mailowo.

<a href="https://codeweek2022-konkurs.webankieta.pl/"><img src="https://codeweek-s3.s3.eu-west-1.amazonaws.com/images/contest-poland.png"></a><br/>

Chyba nie ma takiego drugiego konkursu, w którym do rozdania jest <strong>1000 nagród</strong>! Do udziału w konkursie namówcie znajomych, rodzinę i kogo sobie tylko życzycie. <strong>Ważne, abyście pamiętali, że 1 wydarzenie = 1 organizator. Zatem jeśli zgłosiliście kilka aktywności w ramach Europejskiego Tygodnia Kodowania, do konkursu „1000 pierwszych organizatorów” zgłaszacie tylko jedno wybrane</strong>.

<h1>Dane osobowe</h1>

W formularzu <strong>zbieramy Państwa dane osobowe</strong>, które posłużą do weryfikacji warunków udziału w konkursie, a następnie - w przypadku wygranej - do wysyłki nagrody. Zgodnie z Regulaminem konkursu <strong>podanie danych osobowych</strong> jest dobrowolne, lecz ich niepodanie uniemożliwia udział w Konkursie. W przypadku wycofania zgody na przetwarzanie danych osobowych w celach określonych w Regulaminie przed dniem wysyłki nagrody lub żądania ich usunięcia, Organizator wyklucza taką osobę z udziału w Konkursie, co jest równoznaczne z odmową przyznania nagrody.
Informacje o przetwarzaniu danych znajdziecie Państwo się w <strong>Regulaminie konkursu</strong>. Prosimy o zapoznanie się z nimi <strong>przed przesłaniem formularza</strong>.

Dowiedz się więcej o CodeWeek przechodząc na <a href="https://www.gov.pl/web/koduj/codeweek">polską stronę CodeWeek</a> oraz na oficjalną <a href="https://codeweek.eu/">stronę EU CodeWeek</a>. Po więcej informacji zaglądaj też regularnie na Facebooka <a href="https://www.facebook.com/CodeWeekPL/">Code Week Pl</a> i Instagrama <a href="https://www.instagram.com/codeweekpl/">codeweekpl</a>.


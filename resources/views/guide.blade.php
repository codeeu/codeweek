                <h2>#codeEU toolkit</h2>
                <h2>What is EU Code Week?</h2>
                <p>EU Code Week is a grass-root movement run by volunteers who promote coding in their countries as <a
                            href="/ambassadors">Code Week Ambassadors</a>. Anyone – schools, teachers, libraries, code
                    clubs, businesses, public authorities – can organise a #CodeEU event and add it to the <a href="/">codeweek.eu</a>
                    map.</p>
                <h2>Who can organise a Code Week event?</h2>
                <p>Anyone can organise a Code Week event. </p>
                <ul>
                    <li><strong>Kids/teenagers/adults</strong> can organise coding events to show others how you create
                        with code.
                    </li>
                    <li><strong>Schools / after school clubs / adult evening classes</strong> can organise events for
                        their students. Coding online or unplugged is fun and teaches computational thinking!
                    </li>
                    <li><strong>Teachers and librarians</strong> who code can hold coding classes, share their lessons
                        plans, organise workshops for colleagues.
                    </li>
                    <li><strong>Teachers and librarians who don't code</strong> can invite parents or students to teach
                        participants coding.
                    </li>
                    <li><strong>Coders</strong> can organise workshops in local schools, hack spaces or community
                        centres and invite people to create with code.
                    </li>
                    <li><strong>Code clubs</strong> can organise workshops for new participants and show then how to
                        make a game or an app with code.
                    </li>
                    <li><strong>Businesses and non-profit organisations</strong> can host coding workshops, lend their
                        staff to coach at different events, organise fun coding challenges, offer sponsorship for coding
                        events.
                    </li>
                </ul>

                <h2>What do you need?</h2>
                <ul>
                    <li><strong>A group of people willing to learn.</strong> For example, your friends, kids, teenagers,
                        adults colleagues, friends, parents or grandparents. Remember, two is a group already!
                    </li>
                    <li><strong>Coaches or trainers</strong> who know coding, how to teach and inspire others. The
                        number depends on the type and size of the event. For hands-on workshops, you may need one coach
                        per 5-8 participants, but it all depends. Participants can also help each other! For larger
                        events, it might be a good idea to have a host that makes sure everyone has what they need and
                        keep things running smoothly.
                    </li>
                    <li><strong>A place to be.</strong> Classrooms, libraries, conference rooms and various public
                        spaces all make a great event venue.
                    </li>
                    <li><strong>Computers.</strong> Depending on your target group, you might ask participants to bring
                        their own laptops, in which case don't forget to provide enough power outlets.
                    </li>
                    <li><strong>Internet connection</strong> – WiFi or fixed connections. Make sure your connections
                        will be able to handle the traffic coming from your group
                    </li>
                    <li><strong>Coding unplugged.</strong> You actually don't need computers and internet connection to
                        learn computational thinking. Try out some off-line coding with our <a
                                href="http://codeweek.it/codyroby/">Cody-Roby</a> kit.
                    </li>
                    <li><strong>Something to work with and learn.</strong>Show participants how fun it can be to create
                        something on their own. Check out our <a href="http://codeweek.eu/resources/">list of
                            resources</a> and search the web for existing lesson plans, workshop programs and adjust
                        them to your group's needs. If you have existing computer equipment at the venue, make sure they
                        already have the necessary software installed and provide participants with instructions on how
                        the installation can be done on their own devices.
                    </li>
                    <li><strong>Register participants.</strong> If you have limited spaces available, you can use tools
                        like online forms like Wufoo, Google Forms or event pages on Facebook or Eventbrite to collect
                        registrations. While we do favour free to attend events, you can charge a small fee to cover the
                        costs of the event. Alternately, you can turn to local IT companies or startups for sponsorship.
                    </li>
                    <li><a href="/add">Pin your event</a> on the <a href="/">Code Week map</a>!</li>
                </ul>


                <h2>How to organise your event?</h2>
                <ul>
                    <li>The format of your coding event is up to you, but we do recommend that you include some <strong>practical,
                            hands-on time</strong>, where participants can create on their own and, or tinker with some
                        hardware.
                    </li>
                    <li>You can use whatever <strong>tools and technologies</strong> you like, but we do favour <a
                                href="http://codeweek.eu/resources/">freely available open source material</a>.
                    </li>
                    <li><strong>Smile!</strong> A friendly atmosphere will break the ice and motivate learning.</li>
                    <li><strong>Snacks and drinks</strong>. If your event is a couple of hours your participants will
                        need a break. If you can't provide snacks and drinks, ask participants to bring and share with
                        each other.
                    </li>
                    <li>Ask participants to <strong>show and present</strong> what they have created to each other at
                        the end of your event. Makes everyone proud!
                    </li>
                    <li><strong>Talk about your event!</strong> Talk about your event on social media. Use #CodeEU and
                        @CodeWeekEU. Talk to your friends, the local press, make a press release!
                    </li>
                    <li>Don't forget to <a href="/add">add your event</a> on the <a href="/">Code Week map</a>!</li>
                </ul>


